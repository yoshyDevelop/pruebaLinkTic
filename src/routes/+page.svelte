<h2>Navegación</h2>
<p>En la parte superior de la aplicación, encontrarás una barra de navegación con el título "Prueba-LinkTic". A la derecha de esta barra, hay un botón de alternancia (toggle) representado por tres líneas horizontales.</p>

<p>Al hacer clic en este botón, se desplegará un panel lateral (también conocido como "offcanvas") desde el lado derecho de la pantalla. Este panel contiene las rutas principales de la aplicación:</p>

<ul>
    <li><strong>Inicio</strong>: Esta es la página de inicio de la aplicación.</li>
    <li><strong>Productos</strong>: Aquí puedes ver la lista de todos los productos.</li>
    <li><strong>Pedidos</strong>: Esta página muestra todos los pedidos realizados.</li>
    <li><strong>Clientes</strong>: Aquí puedes ver la información de todos los clientes.</li>
</ul>

<p>Para navegar a cualquiera de estas páginas, simplemente haz clic en el enlace correspondiente en el panel lateral.</p>

